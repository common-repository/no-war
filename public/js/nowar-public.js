function NoWarUpdateStatus()
{
	var xhttp = new XMLHttpRequest();
	xhttp.onreadystatechange = function() {
		if (this.readyState == 4 && this.status == 200) {
			var eflag = document.getElementsByClassName("nowar-flagHolder")[0];
			eflag.style.display = "none";
		}
	};
	xhttp.open("GET", "?nowar=true", true);
	xhttp.send();
}
